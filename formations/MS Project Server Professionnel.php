							<div class="tab-pane" id="exercice">
								<p>
                                  Comme pour tant d’aspects des systèmes d’entreprise, le premier problème consiste à définir précisément ce qu’il faut entendre par « gestion des ressources ». Selon le point de vue, le concept de gestion des ressources peut recouvrir différentes choses.<br>
<strong>Nivellement des ressources</strong><br>

Commençons par le point de vue du puriste. Cette définition provient de ceux d’entre nous qui travaillent dans le secteur depuis plus de 15 ans. Le premier logiciel de gestion de projets commercialisé était un outil de planification selon la méthode du chemin critique (CPM). Les premiers systèmes de ce type ne tenaient aucun compte des ressources. Il était déjà très excitant de pouvoir générer un planning à partir du système, puis, lorsque les traceurs à rouleau ont fait leur apparition (essentiellement destinés au secteur du dessin technique), d’utiliser ceux-ci pour obtenir un histogramme ou un diagramme logique (réseau) représentant nos projets.

Les ressources furent factorisées dans ces systèmes pour améliorer les calculs d’origine par l’ajout des ressources. Les algorithmes variaient, mais le but était de veiller à ce que tout retard résultant d’un nombre insuffisant de certaines compétences soit identifié suffisamment longtemps à l’avance pour permettre d’embaucher les personnes nécessaires. Nous reviendrons au nivellement des ressources un peu plus tard
								</p>
							</div>
							
							<div class="tab-pane" id="competence">
								<p>
                                     En développant un peu notre pensée, nous pouvons inclure ces deux premières définitions dans un concept plus large de gestion de la capacité des ressources. Il s’agit de loin du concept le plus populaire que nous soyons appelés à développer dans un déploiement de gestion de projet d’entreprise. La planification de la capacité des ressources vise à apporter aux décideurs d’une organisation des réponses à plusieurs questions :

    Puis-je respecter les engagements que j’ai déjà pris à l’aide du personnel dont je dispose ?

    Si ce n’est pas le cas, quels changements dois-je apporter au personnel pour respecter mes engagements ?

    Si c’est le cas, quelles capacités excédentaires puis-je consacrer à des engagements supplémentaires ?

    Si je reçois un projet supplémentaire, puis-je l’accomplir avec mon personnel existant ?

    Si je ne peux pas modifier mon personnel, quel sera l’impact d’un nouveau travail à accomplir ?

La planification de la capacité des ressources englobe plusieurs concepts qui doivent être mis en œuvre dans le cadre de vos processus de gestion de projets. Ces concepts sont les suivants :<br>

    <strong>Chargement des ressources</strong><br>

    Pour relever le défi de la planification de la capacité des ressources, nous pouvons commencer par nous interroger sur la quantité de travail à accomplir. S’il n’y a rien à faire, il est inutile de gérer les ressources. Pour effectuer une analyse précise, il est essentiel d’avoir une idée exacte de la quantité de travail nécessaire pour l’exécution de chaque tâche, et des personnes qui devront l’accomplir.<br>

    <strong>Planification des compétences</strong><br>

    Les outils de gestion de projet modernes tels que Microsoft Project nous permettent de gérer les besoins de ressources en termes de département ou de personnes, mais aussi de compétences. La planification des compétences comprend la gestion des besoins de ressources, ainsi que la gestion de la disponibilité des compétences sous forme d’inventaire. Cela semble en effet globalement plus efficace que la simple gestion du nombre de personnes disponibles dans chaque département. Après tout, si vous avez un département de gestion de bases de données dont le personnel n’est pas compétent en SQL Server 2008, alors que de telles compétences sont requises pour votre prochain projet, ces compétences feront nécessairement défaut au moment de réaliser le projet.<br>

    <strong>Affectation des ressources</strong><br>

    Qui décide de l’affectation des ressources ? Il convient de créer un processus permettant de déterminer la manière dont les besoins de ressources sont définis, puis résolus. Devez-vous commencer par demander une catégorie ou une compétence de ressource ? Devez-vous formuler des demandes de ressources en mode « Proposé », puis demander à quelqu’un disposant de l’autorité nécessaire de les valider en mode « Engagé » ? Devez-vous avoir différents projets avec des ressources proposées qui soient conservées séparément ? Un responsable de projet doit-il pouvoir engager de sa propre initiative des ressources de différents départements ou seuls les chefs de département doivent-ils avoir ce privilège ?<br>

    <strong>Disponibilité des ressources</strong><br>

    Vous avez des projets nécessitant des ressources, mais quelles sont les ressources disponibles ? Qui décide du temps que chaque ressource consacrera au projet ? Qui décide du moment des congés ? Comment déterminer le nombre d’heures supplémentaires pouvant être ouvrées quotidiennement ? Comment gérer les heures supplémentaires non rémunérées ? Seront-elles converties en crédits d’heures ?<br>

    <strong>Nivellement des ressources</strong><br>

    Lorsque vous disposez des informations relatives à la disponibilité et aux besoins des ressources pour vos projets, la comparaison entre ces deux éléments vous permet d’épingler d’éventuels problèmes d’affectation excessive et de tenter de les résoudre. Les projets doivent-ils être hiérarchisés de façon à ce que certains obtiennent les ressources nécessaires et d’autres non ? Les tâches doivent-elles être hiérarchisées de façon à ce que certaines obtiennent les ressources nécessaires et d’autres non ? Devez-vous utiliser un nivellement automatique des ressources ? Qu’en est-il du déplacement manuel des tâches ? Si certaines parties de l’entreprise attendent l’exécution d’un travail par d’autres parties, qui se s’occupera de régler le conflit ?

