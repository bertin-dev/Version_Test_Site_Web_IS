								<p>La gestion des ressources est la principale raison pour laquelle les organisations passeront de la gestion de projet individuel à la gestion de projet d’entreprise avec des systèmes tels que Microsoft Office Project Server.
                                Penser cela signifierait que nous disposions d’une méthode infaillible pour obtenir une gestion des ressources sans égale à partir de tels systèmes. 
								</p>
