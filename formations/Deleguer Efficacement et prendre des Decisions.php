Déléguer des tâches au personnel et aux bénévoles fait partie intégrante de ce que nous faisons quotidiennement comme dirigeants d’association. Compte tenu que la délégation constitue un élément central du travail quotidien de la gestion d’association, pouvons-nous par conséquent présumer que nous sommes plutôt doués à cet égard?  Il est permis de penser que la réponse est non!  Nous sommes, semble-t-il, assez bons pour confier des travaux à d’autres, mais nous parvenons rarement à déléguer efficacement et stratégiquement. Qui plus est, la délégation n’est pas une habileté sur laquelle nous nous concentrons ou même réfléchissons, or il s’agit d’une compétence susceptible de faire la différence entre une association à rendement élevé ou non. <br>
Les études appuient fortement ce constat et indiquent que les avantages à long terme de l’application appropriée de cette habileté peuvent être substantiels. Dans son article intitulé 12 Rules of Delegation (Les 12 règles de la délégation), Richard Lannon explique qu’une délégation efficace peut faire gagner temps et argent aux associations, aider à la planification successorale et contribuer à bâtir les compétences et motiver les gens. Une délégation déficiente peut cependant causer de la frustration et de la confusion.  Compte tenu de son impact sur l’efficacité organisationnelle, la délégation efficace constitue une importante aptitude de gestion sur laquelle il vaut le peine de se pencher un peu plus.<br>
<strong>Définir la délégation</strong><br>
Afin de mieux comprendre comment appliquer cette habileté, il est important de comprendre d’abord ce que signifie la délégation.  Selon Wikipédia, « la délégation est la réunion d’autorité et de responsabilité confiées à une autre personne afin de mener à bien des activités précises.  C’est l’un des concepts clés du leadership de gestion ».  Cela signifie que la délégation implique de faire confiance à d’autres. Aussi simple que cela puisse paraître, ce peut être une perspective terrifiante pour certains gestionnaires. Cela implique de donner à autrui le pouvoir de prendre des décisions de façon autonome et d’assumer la responsabilité de certaines tâches.  Par contre, si quelque chose tourne mal, le gestionnaire est tenu responsable en bout de ligne.  La délégation revêt par conséquent une importance capitale.<br>
Au contraire de la pratique répandue, la délégation n’est pas uniquement une question de dire quoi faire à quelqu’un.  Ce n’est pas non plus une question de tout ou rien.  Les gestionnaires doivent prendre conscience qu’il existe, et qu’il devrait exister, divers degrés de délégation, selon la nature de la tâche et la personne à qui on la délègue.  Cela signifie parfois aussi que la délégation est davantage un processus qu’une action.  Simple en théorie, cela représente toutefois un changement important par rapport à ce que l’on entend habituellement par délégation, soit de donner simplement du travail, et implique de passer à un monde d’accompagnement et de mentorat. <br>
La délégation devient ainsi partie intégrante d’une stratégie à plus long terme susceptible d’apporter des bénéfices organisationnels plus vastes en maximisant le potentiel des ressources humaines.  Autrement dit, la délégation efficace est essentielle à un leadership efficace.  Cela représente donc certains défis potentiels en ce qui a trait au processus et à l’engagement de temps que comporte une délégation efficace.<br>
