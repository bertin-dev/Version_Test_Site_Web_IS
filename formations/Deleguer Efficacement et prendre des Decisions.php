								<p>
								D’un point de vue organisationnel, la délégation efficace sert deux objectifs.  Premièrement, elle aide à faire en sorte de maximiser l’utilisation de nos bénévoles et notre personnel, à court et à long terme.   Deuxièmement, elle nous donne l’occasion de perfectionner nos gens. En fait, le temps est l’une des ressources les plus précieuses des associations dans le monde actuel.  La délégation efficace nous donne la capacité de maximiser ce temps.  Ainsi, investir dans des processus de délégation efficace doit être vu comme un investissement non seulement dans nos gens, mais aussi dans la santé et le succès à long terme de nos associations. 
								
								</p>
							</div>
							
							<div class="tab-pane" id="competence">
								<p>
								1) Définir et sélectionner les tâches. La première étape consiste à définir les tâches à accomplir et à déterminer lesquelles d’entre elles il est approprié de déléguer.  De toute évidence, le gestionnaire devra conserver la responsabilité de certaines tâches et fonctions de gestion cruciales à son rôle et la délégation sera donc inappropriée dans ce cas.  Il est important d’adopter une vision à long terme à l’égard du processus de sélection.  Les gestionnaires devraient viser à déléguer le plus possible de façon à perfectionner en bout de ligne leur personnel et leurs bénévoles, et les amener à des degrés et des habiletés plus élevés. <br>

 

2) Sélectionner la personne (ou équipe). La seconde étape consiste à décider à qui déléguer les tâches définies.  Il faut tenir compte du savoir, des habiletés, de l’expérience, du style de travail et, bien sûr, de la charge de travail de la personne (ou équipe).  Bref, afin de déléguer efficacement, il importe de comprendre d’abord nos gens.  Il est également important de se demander si la tâche procure une occasion de grandir et de perfectionner les habiletés.<br>

 

3) Évaluer les besoins de formation.  Après avoir identifié la tâche et la personne (ou équipe), nous devons ensuite évaluer les besoins de formation, les degrés d’habileté et nous assurer de répondre à certains critères.  Premièrement, la personne (ou équipe) doit savoir ce qu’on attend d’elle.  Deuxièmement, elle doit avoir l’autorité pour faire ce qu’on lui demande de faire.  Enfin, elle doit savoir comment exécuter ce qu’on lui demande de faire. <br>

 

4) Fixer des buts, des objectifs et des échéanciers clairs. Comme dans le cas de la fixation d’objectifs, les tâches déléguées sont effectuées le plus efficacement lorsqu’elles sont « SMARTER ».  Ce mnémonique fréquemment employé signifie : spécifique, mesurable, consensuel, réaliste, limité dans le temps, éthique et consigné.  Fixer des buts, des objectifs et des échéanciers clairs est essentiel pour assurer que les personnes à qui on délègue connaissent exactement ce qu’on attend d’eux.  Cela aide également à limiter le risque de microgestion.<br>

 

5) Assurer l’accès aux ressources et à l’information.  La délégation efficace se produit uniquement si les personnes à qui on délègue ont accès à l’information dont ils ont besoin pour réussir.  Contrôler et limiter l’accès à des renseignements pertinents nuit toujours à tout processus de délégation efficace. Il est essentiel que les personnes à qui on délègue, qu’il s’agisse du personnel ou de bénévoles, aient accès au savoir requis.<br> 

 

6) Soutien, communication et rétroaction continus.  En dernier lieu, la communication doit être ouverte, claire et permanente.  Les gestionnaires doivent s’assurer de donner régulièrement une rétroaction exacte et ponctuelle.  Il importe que les gens sachent comment ils se débrouillent, et s’ils ont atteint leurs buts prédéfinis. En cas de problème, il est important de s’assurer qu’ils comprennent le problème, qu’ils se sentent suffisamment confiants pour poursuivre, et qu’ils sachent comment prévenir des problèmes ou erreurs subséquents. Et une fois le travail terminé, et bien exécuté, il est important de prendre le temps de reconnaître et de célébrer le succès! <br>
								
