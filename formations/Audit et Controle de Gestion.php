								<p><h3> Les missions et activités du contrôle de gestion</h3> 
									<strong>Objectifs : </strong>  « Il permet d’assurer, tout à la fois, le pilotage des services sur la base d’objectifs et d’engagements de service et la connaissance des coûts, des activités et des résultats. »
									
									
									
									<strong>Activités : </strong>
									Globalement, les activités de la fonction se répartissent en deux grands domaines relatifs :<br> 

à l’aide au pilotage et à la prise de décision ;<br> 
à la mesure et l’analyse des activités, des coûts et des résultats et au développement d’outils de programmation des activités et de justification au premier euro.<br>
<strong>L’organisation et l’animation de la fonction au sein de l'entité concernée (ministère, direction d'administration centrale, service déconcentré...) visent en tant qu'activités spécifiques à assurer la cohérence globale et l'efficacité du contrôle de gestion.</strong>

 

<h5>1. Aider au pilotage stratégique et opérationnel, et à la prise de décision</h5>

formaliser les objectifs et indicateurs de performance des programmes et garantir leur déclinaison opérationnelle ;<br>
organiser le dialogue de gestion autour de la recherche de la performance (moyens, objectifs, résultats) et contribuer au processus budgétaire (dans ses différentes phases : construction, exécution, bilan) ;<br>
produire les rapports de gestion et les tableaux de bord, à partir des données provenant des services gestionnaires, pour aider à la prise de décision et contribuer à l’efficacité et l’efficience des processus de management.<br>
<h5>2. Assurer la mesure des activités, des produits, des coûts et des résultats</h5>

définir les nomenclatures des activités et des produits ;<br>
mesurer les activités, les produits, les coûts et les résultats ;<br>
définir et mettre en œuvre les méthodes de comparaison entre unités et les méthodes d’étalonnage ;<br>
développer les outils de justification « au premier euro » et de programmation des activités.<br>
<h5>3. Organiser et animer la fonction de contrôle de gestion</h5>

structurer la fonction, définir et documenter l’organisation, les outils et les procédures de contrôle de gestion ;<br>
assurer la maîtrise d’ouvrage des systèmes d’information nécessaires au contrôle de gestion (collecte de données élémentaires, consolidation, analyse) ;<br>
mutualiser les bonnes pratiques (réunions, séminaires..) ;<br>
définir les profils de poste et les référentiels de compétence liés à la fonction contrôle de gestion ; conduire le changement autour de la mise en œuvre de cette fonction ;<br>
définir les besoins de formation en contrôle de gestion et valider les contenus pédagogiques des stages proposés ;<br>
définir et actualiser régulièrement les contenus de la documentation ministérielle en ligne sur le contrôle de gestion ;<br>
animer les réseaux internes de contrôleurs de gestion (administration centrale, services déconcentrés, opérateurs) ;<br>
communiquer sur le contrôle de gestion (diffuser au sein du ministère les informations en provenance du réseau interministériel).<br>
								</p>
