            <p class="lead">L'audit est une activité de contrôle et de conseil.<br>
			Elle consiste à mettre en place une expertise par un agent compétent et impartial et à porter un jugement sur l'organisation, la procédure, ou une opération quelconque de l'entité.<br>

            Outil d'amélioration continue, l’Audit permet de faire le point sur l'existant (état des lieux) afin d'en dégager les points faibles et/ou non-conformes (suivant les référenciels d'audit). Cela, afin de mener par la suite les actions adéquates qui permettront de corriger les écarts et dysfonctionnements constatés.<br>
            Principalement exercée en cabinet, la profession d’auditeur a évolué afin de mieux répondre aux exigences du marché en termes de transparence, de lisibilité des états financiers, de confort en matière de contrôle interne…<br>
