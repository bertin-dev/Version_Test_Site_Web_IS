								<p>
								<strong>Animer une antenne de contrôle de gestion liée aux responsabilités opérationnelles.</strong>
                                     Les auditeurs qui veillent à la conformité légale et à l’optimisation des processus liés à l’activité économique de l’entreprise.
Ils interviennent dans le cadre de missions ponctuelles (audit légal, contractuel, contrôle interne, risk management, etc. ). Les principaux employeurs sont en général les cabinets d’audit ou les sociétés de conseil (audit externe), mais également les grands groupes (audit interne).<br>
Seuls ou en équipe, ils sont rattachés à la direction générale, la direction de l’audit dans les grands groupes, ou à un directeur de mission dans le monde du conseil.
Ils ont pour missions de :<br>
de diagnostiquer les dysfonctionnements dans les procédures de gestion, d’organisation ou dans les méthodes de travail (contrôle de la fiabilité de l’information financière, qualité des process, contrôle des comptes, mesure des risques légaux et réglementaires, etc. )<br>
d’apporter des recommandations au commanditaire (la direction générale) en matière d’améliorations ou de réajustements à effectuer au regard d’impératifs réglementaires ou de performances.<br>
