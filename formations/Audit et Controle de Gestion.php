								<p><strong>Missions : </strong>
									Dans ce nouveau cadre, les missions du contrôle de gestion se font plus précises :<br>
									<mark>analyser la performance</mark> des activités afin d’optimiser leur pilotage : dans cette optique, le contrôle de gestion doit essentiellement apporter les outils de connaissance des coûts, des activités et des résultats permettant d’améliorer le rapport entre les moyens engagés et l’activité ou les résultats obtenus ;<br>
									
									<mark>nourrir le dialogue de gestion</mark> entre les différents niveaux de responsabilité (responsables de programmes, responsables de budgets opérationnels de programme, responsables d’unité opérationnels, opérateurs) : le nouveau cadre de gestion en mode LOLF implique la mise en oeuvre d’un dialogue de gestion favorisant l’expression des acteurs les plus proches du terrain et la diffusion des bonnes pratiques. Le contrôle de gestion vient documenter et objectiver ce dialogue.<br> </p>
							</div>
							
