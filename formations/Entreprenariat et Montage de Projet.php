								<p>    
								<strong>Les compétences de l'entrepreneur</strong>

Le chef d'entreprise doit avoir certaines aptitudes et compétences mais il doit surtout savoir ce qu'il ne sait pas ou ne veut pas faire et trouver les solutions ailleurs (recruter, sous-traiter, externaliser).<br>
Un entrepreneur est « une personne capable de transformer un rêve, une idée, un problème ou une occasion en une entreprise ». (Paul-Arthur Fortin)
Des chercheurs dont Alain FAYOLLE ont défini les compétences entrepreneuriales requises pendant chaque étape d'une création puis d'une gestion d'entreprise.<br>
<strong>Les compétences du créateur d'entreprise</strong>

Pendant la phase de préparation du projet, les compétences et aptitudes mobilisées seront :<br>

    des aptitudes émotionnelles : motivation, volonté, tolérance au risque, autonomie, implication,<br>
    des aptitudes de perception des opportunités : pour transformer des opportunités en activités rentables,<br>
    des compétences en maîtrise de l'information car vous allez devoir chercher de nombreuses informations, <br>
    des capacités d'absorption car vous allez être submergé d'informations et il faudra trier ! (les CCI peuvent vous y aider),<br>
    des compétences en gestion de la nouveauté, en gestion des paradoxes,<br>
    des compétences en méthode et conduite de projet,<br>
    une envie et capacité à apprendre.<br>

<strong>Les compétences du chef d'entreprise</strong><br>

Pour gérer et développer l'entreprise, d'autres compétences devront être mobilisées :<br>

    des compétences techniques :<br>
        Gestion administrative : Factures, devis, TVA, URSSAF, contribution économique, territoriale, RSI, DUE, BIC, BNC, IS, INPI, Greffe, rangement, classement… ça vous évoque quelque chose ? En rythme de croisière, comptez entre 3 et 4 heures d'administratif « pur » par semaine. Mais beaucoup plus à vos débuts !<br>
        Comptabilité – gestion – finance : BFR, emplois, ressources, charges, investissements, dotations aux amortissements, amortissements linéaires et dégressifs, rentabilité, FI-FO, valeur ajoutée, solde intermédiaire de gestion, passif, actif, marge brute, marge nette, tableau de bord, trésorerie…C'est clair ? Sinon, des formations existent.<br>
        Ressources humaines - Droit du travail : 35 heures, heures supplémentaires, convention collective, DUE, entretien préalable au licenciement, RTT, congés, entretien d'embauche, fiche de poste, critères de sélection… Si vous voulez recruter, connaître les bases en droit du travail et en gestion des ressources humaines est indispensable !Cela vous permettra d'acquérir des réflexes et éviter de faire appel à un conseil juridique systématiquement. Alors, formez-vous !<br>
        Juridique - Droit des sociétés : Conditions générales de vente, bail commercial, contrat commercial, CNIL... beaucoup d'éléments de l'entreprise sont juridiques.Formez-vous à l'essentiel et sous-traitez.<br>
        Commercial : Inné ou acquis ? Aimer vendre, c'est plutôt inné mais bien vendre, ça s'acquiert ! Concevoir et mettre en œuvre une politique commerciale adaptée à votre entreprise (même si elle est toute petite), négocier, convaincre les clients, les fidéliser…Ca s'apprend.
        Management : Là encore, il y a de l'innée et de l'acquis. Aimer motiver des équipes, les diriger, être le leader, c'est plutôt inné. Mais il existe des techniques de conduite de réunion, de délégation, de motivation d'équipes, de management général…<br>

    des compétences stratégiques,<br>
    des compétences sociales et relationnelles,<br>
    des compétences de développement de projet,<br>
    diverses aptitudes dont :<br>
        Goût de l'action<br>
        Esprit d'initiative<br>
        Orientation vers le résultat<br>
        Persévérance<br>
        Résistance<br>
        Talent de persuasion<br>
        Esprit de décision<br>
        Rigueur<br>
        Ténacité<br>
        Sens du client<br>
        Capacité d'écoute<br>
        Esprit d'analyse<br>
        Esprit de synthèse<br>

En tout état de cause, la qualité principale d'un dirigeant est VENDRE et donc trouver des clients. Mais attention toutefois, vendre, c'est bien mais vous ne devrez pas négligez ce qui en découle : faire les devis, les factures, relancer les clients, vérifier les finances de l'entreprise...<br>
								
