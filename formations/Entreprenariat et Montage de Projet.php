								<p>


Après 15 années d’expérience, je constate que les préoccupations d’un candidat à la création ou la reprise d’entreprise se focalisent principalement sur le montage juridique de son activité, l’optimisation fiscale et sociale, les démarches administratives d’installation, les aides financières diverses pour le lancement de l’activité.
Interrogations, certes importantes mais qui ne systématisent pas la réussite d’un projet.
En effet, si vous envisagez un projet de création ou de reprise d’entreprise, ne brûlez pas les étapes ! Concentrez-vous d’abord sur l’analyse de votre marché et attachez-vous à construire une offre qui vous assurera des opportunités commerciales.
Vérifiez également “votre potentiel entrepreneurial” (vos motivations, vos compétences et vos capacités pour entreprendre).
Le business plan ou le plan d’affaires*, aussi parfait soit-il, n’est pas suffisant pour réussir. Il ne peut être porté et mis en oeuvre que par des hommes et des femmes. C’est pour cette raison que nous croyons avant tout au “capital humain” et que nous centrons notre démarche d’accompagnement sur la personne et son environnement.
</p>
