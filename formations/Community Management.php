								<p>
								Devant l’importance des avis échangés entre consommateurs-internautes sur les marques et la menace que représentent les réseaux sociaux et autres regroupements de consommateurs sur le net, les entreprises ont compris qu’il est indispensable de savoir ce qui se dit sur elles. Et maintenant de savoir le gérer.<br>
								C’est là toute la mission du Community manager qui est un créateur de liens entre la marque et les utilisateurs, en étant à la fois le porte-parole des usagers en interne (Dirigeants, actionnaires…) et celui de la marque en externe.<br>
								Il va d’une part assurer une veille sur le web, être à l’écoute et faire un état des lieux de la désormais fameuse e-réputation de sa société. Il scrute ainsi régulièrement les sites, forums, blogs en rapport avec l’activité de son entreprise, en extrait les informations pertinentes et propose un plan d’actions et des solutions en cas de problème (critique, crise…). Pour ce faire, il instaure un dialogue avec les internautes pour résoudre le problème le plus rapidement possible et à moindre coût.
								</p>
							</div>
							
							<div class="tab-pane" id="exercice">
								<p>
                                   <strong>La fonction de Community Manager</strong> possède une part éditoriale forte puisqu’il s’agit de créer et de fournir du contenu digital (action correspondante au terme technique de SMO, pour Social Media Optimisation). Elle est l’évolution naturelle du poste de Content Manager, et de nombreuses autres passerelles existent pour arriver à ce métier comme : chef de projet éditorial, journaliste web, référenceur, etc.tielle est celle de fédérer et de fidéliser les internautes autour de la marque en créant une communauté conviviale qu’il doit également animer. Il va diffuser du contenu informatif sur les sites afin de rendre sa société accessible et « humaine » pour tous les consommateurs. Il dialogue avec eux, répond à leurs questions, gère les UGC (User Generated Content) en modérant les contributions, les informe par des newsletters, anime les débats, créé des événements (tchats, concours…).</br>
								   Il assure en outre la visibilité de son entreprise auprès des réseaux sociaux par la création de groupes, de hubs ou de contenus viraux sur des sites tels que Facebook, Myspace, Viadeo, Youtube… Son objectif est aussi de rendre la marque plus visible en augmentant le nombre de liens et de billets ou de commentaires.
                                   Enfin, il peut intervenir sur le développement de la notoriété de sa société par la réalisation d’actions webmarketing : référencement du site, netlinking, partenariats entre sites web, affiliation…
                                   Féru de technologies et à l’affût des tendances du web, il détecte tous les nouveaux outils (ex. : Twitter, applis Iphone, FriendFeed…) qui peuvent favoriser une relation de proximité avec sa/ses communautés.
                                   Officiellement, le Community Manager intervient toujours au nom de son entreprise et ne se fait pas passer pour un faux consommateur.
								</p>
							</div>
							
							<div class="tab-pane" id="competence">
								<p>
                                    On trouve les Community Manager principalement dans le secteur des high tech au sein de start-ups ou de grands groupes où il dépend du département marketing ou produit.
                                    Ils peuvent également exercer au sein d’agences de communication spécialisées (dont certaines, devant l’émergence du métier, ont tout spécialement créé des structures dédiées à ce domaine).
                                    De par la sensibilité de la mission, cette fonction est le plus souvent attribuée à un communicant ou marketeur expérimenté… et parfois, c’est même le dirigeant de l’entreprise.<br>
									C’est une fonction très sensible, qui demande beaucoup d’habileté, de diplomatie et de réactivité. La personne qui l’occupe peut être facilement sur un siège éjectable.
                                    Par ailleurs, fonction encore peu commune, elle est souvent à cheval sur plusieurs compétences : marketing, relations-publiques, éditorial… Son rôle est donc difficile à délimiter et est en constante évolution.<br>
									Certaines compétences sont indispensables pour mener à bien ses objectifs :<br>
                                      Une solide culture web et des tendances internet<br>
                                      Une totale compréhension des mécanismes communautaires<br>
                                      Une connaissance et une maîtrise des différents réseaux sociaux et des outils liés<br>
                                      Un sens du contact, de la relation client et de la diplomatie<br>
                                      Une aisance rédactionnelle<br>
                                      La connaissance des règles de base en référencement (SEO et SMO)<br>
                                      La compréhension des principes de base du marketing, de l’e-marketing et de la communication<br>
                                      Une connaissance approfondie de l’entreprise ou du client et de ses valeurs<br>
                                      La réactivité / capacité à travailler plusieurs tâches en même temps<br>
                                      Une capacité d’adaptation à un environnement en perpétuelle mutation<br>

 
								</p>
