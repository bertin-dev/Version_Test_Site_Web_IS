								<p>Une charte graphique regroupe et traduit graphiquement tout l’univers et toutes les valeurs de l’entreprise (dynamisme, innovation, élégance, …) , c’est le support fondamental de toute sa communication (interne et externe). L’élément central d’une charte graphique est le logo (les logos symboles, les logos typographiques, et les logos mixtes), on le retrouve sur tous les supports de communication de l’entreprise, c’est à partir du logotype que l’on bâti une identité visuelle.<br>
                                Le logotype doit refléter l’image de l’entreprise et ses activités, il doit également renforcer son image et rassurer ses clients. Le logo doit pouvoir s’adapter et se décliner (différentes couleurs, avec ou sans baseline, carré ou rectangulaire,…) sur l’ensemble des supports de communication de l’entreprise. On le retrouvera par exemple sur le papier à en-tête, les devis et factures, les affiches, les plaquettes commerciales, les flyers, le site Internet et les véhicules de l’entreprise.<br>
								Le logotype à lui seul ne constitue pas pour autant une charte graphique, la charte définit également les polices de caractères et les attributs typographiques à utiliser, en règle générale une police de titre et une police de contenu. La typographie est une des composantes essentielles de l’univers graphique d’une entreprise, certaines entreprises comme Renault vont jusqu’à créer une police de caractères spécifique pour se démarquer.<br>

                               La charte fige aussi les choix de couleurs au travers de jeux de couleurs adaptés aux exigences des différents supports de communication et des différents fonds disponibles (fond de couleur ou fond blanc). Le nombre de jeux de couleurs doit rester limité pour simplifier la mémorisation de l’identité visuelle et sa différenciation par rapport à la concurrence (4 ou 5 couleurs étant un compromis idéal entre mémorisation et différenciation) . On doit retrouver dans la charte les valeurs Pantone, CMJN, RVB et hexadécimales de chaque couleur utilisée.<br>
							   
							   <h3>Une charte graphique complète regroupe et codifie également :   </h3>
							       Les règles d’insertion de chaque élément (logo, titre, baseline,…) et pour chaque support : les marges et le positionnement dans le document.<br>
                             Les règles éditoriales (ton et style) aidant tout rédacteur à rendre ses documents homogènes.<br>
                              Les règles d’adaptation et de déclinaison pour les supports comme tels que la télécopie, les RIB personnalisés, … en proposant par exemple une version simplifiée du logo (noir et blanc).<br>

                     Certaines chartes graphiques sont conçues pour s’adapter et évoluer en fonction des changements de l’entreprise.
