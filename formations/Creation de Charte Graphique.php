								<p><h3>Les objectifs et les intérêts d’une charte graphique</h3>
								L’un des objectifs d’une charte graphique est de conserver une cohérence graphique dans toutes les réalisations graphiques d’une entreprise. Sa mise en place garantie une identité visuelle homogène que pourront s’approprier les clients ainsi que tous les partenaires (fournisseurs, graphistes, presse,…) d’une entreprise. La charte graphique permet donc de communiquer « d’une seule voix » avec comme conséquence positive une maîtrise de l’image de l’entreprise. Les anglophones parlent de corporate design (littéralement : dessin de l’entreprise).<br>
                                 L’autre objectif de la charte graphique est de permettre au récepteur d’identifier facilement l’émetteur par la cohérence associée à la récurrence des différentes réalisations graphiques. Autre intérêt non négligeable, une charte graphique complète permet d’accélérer et de faciliter la production de nouveaux supports de communication, les fondamentaux étant déjà existants.<br>						
