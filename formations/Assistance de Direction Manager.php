									<strong>Assistance de Direction / Manager </strong>  
                                 Assiste un ou plusieurs responsable(s) (cadre dirigeant, directeur,...) afin d'optimiser la gestion de leur activité (gestion de planning, organisation de déplacements, communication, préparation de réunions, accueil, ...). Organise et coordonne les informations internes et externes, parfois confidentielles, liées au fonctionnement de la structure.<br>
                                 Peut prendre en charge le suivi complet de dossiers (contrats de maintenance des équipements, suivi de relance clients, gestion administrative du personnel ...) ou d'évènements spécifiques (organisation de séminaires, salons, ...).<br>
                                 Peut coordonner une équipe
								 <h3>   Accès à l'emploi métier   </h3>    
								 Il est accessible avec un diplôme de niveau BEPC complété par une expérience professionnelle.<br>  

                                 Un complément de formation de niveau Licence en droit, gestion comptable ou langues peut permettre d'accéder à des postes à responsabilité.<br>  

                                 La pratique d'une ou plusieurs langue(s) étrangère(s) est requise<br>
                                   								 
