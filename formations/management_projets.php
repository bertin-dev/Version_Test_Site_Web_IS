                               Un processus est un ensemble d’actions et d’activités en relation les unes avec les autres, menées à bien pour créer un ensemble prédéfini de produits, de résultats ou de services. Chaque processus est caractérisé par ses données d’entrée, les outils et les techniques applicables et les données de sortie qui en résultent. Comme cela a été expliqué à la section 2, le chef de projet doit tenir compte des actifs organisationnels et des facteurs environnementaux de l’entreprise. Ceux-ci doivent être pris en considération pour chacun des processus, même si la spécification du processus ne les mentionne pas explicitement comme des données d’entrée. Les actifs organisationnels fournissent des directives et des critères permettant d’adapter les processus de l’organisation aux besoins spécifiques du projet. Les facteurs environnementaux de l’entreprise peuvent imposer des contraintes sur les options de management de projet.<br>

