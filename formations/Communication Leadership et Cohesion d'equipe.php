							<div class="tab-pane active" id="activite">
								<p>La culture d’entreprise, la vision du futur, la réflexion stratégique et l’art de leadership permettent dans un team building de découvrir des facettes inexplorées de chaque manager de direction par le recours à des formes d’expressions inhabituelles. Dans le monde de l’entreprise actuel, ces champs d’actions ont pour objectif d’évacuer le stress par l’imagination stratégique mais aussi de favoriser la motivation, de structurer la cohésion d’équipe et de promouvoir l’adhésion aux valeurs phares de l’entreprise. Cette action de nature évènementielle que constitue  la cohésion d’équipe peut être réalisée en inter ou intra entreprise. La formation cohésion d’équipe dirigeante est  un excellent moyen pour entraîner les managers de direction à agir dans la même direction en partageant des valeurs communes :<br>
 
	Améliorer les qualités relationnelles entre les managers du comité de direction,
	Donner le sens à la culture d’entreprise et à l’organisation managériale,
    Concentrer les efforts communs sur la réalisation des objectifs stratégiques,
	Passer de l’équipe de managers d'unités à l’équipe de direction de l’entrepris
 	Développer son leadership dans le respect et la confiance,
	Oser communiquer de façon authentique pour mieux travailler ensemble,
    Décider d’un plan d’action qui renforce les liens et génère le respect mutuel,
    Faciliter la cohésion de ses équipes autour des valeurs phares de l'excellence.<br>
 
Le travail sur la cohésion d’équipe dirigeante est fondée sur 5 principes : le principe lié à l’expertise du consultant ressource, le principe de partage des expériences, le principe de réflexion stratégique, le principe de formalisation des plans d’action, le principe d’engagement. Lors de ce séminaire cohésion d’équipe, chaque manager s’approprie d’un dispositif fédérateur de performance à plusieurs niveaux : le partage de la vision stratégique, le développement des compétences vitales ainsi que les engagements pris au niveau individuel et collectif pour réaliser ses objectifs.
								</p>
