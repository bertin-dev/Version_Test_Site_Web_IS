								<p><h3>L’authenticité</h3>

                               Le leadership, d’abord, c’est quoi ? C’est la capacité d’une personne à influencer et à fédérer un groupe pour atteindre ensemble un objectif commun. Pour cela, le manager doit être lui-même, rester fidèle à ses croyances et à ses valeurs afin d’exercer une influence positive sur les autres. En fait, le bon leader ne doit pas simplement rester fidèle à lui-même, mais également être en mesure de transférer adéquatement ses valeurs et ses croyances à son équipe. « Etre un leader, c’est d’abord savoir s’écouter » 
							   <h3>De bonnes aptitudes en communication </h3>
							   Etre un bon communiquant permet au leader d’être un meilleur négociateur. Un leader n’obtient pas nécessairement l’accord de tous les membres de son équipe pour chacune de ses initiatives. C’est sa capacité à communiquer qui lui permet de gagner en légitimité et de faire agir ses collaborateurs, en leur donnant l’envie de s’engager. Le leader réussit ainsi à obtenir le soutien de chacun, et à donner du sens à leur travail.
							   
							   <h3>Une vision propre à soi</h3>
							   Il est indispensable de transmettre SA propre vision des choses. En formation, notamment chez OPP, on apprend à personnaliser son management pour développer l’implication des collaborateurs. Avec le FIRO-B, un test de personnalité, le manager peut ainsi mesurer ses propres besoins selon 3 grands items : l’inclusion, le contrôle et l’émotion. On affine ensuite les résultats en distinguant ses attentes de ses faits et gestes.  L’objectif est d’aider le manager à se connaître lui-même, avant même qu’il ne manage les autres.
							   
							   
							   
							   <h3>L’empathie </h3>
							   Un bon leader ne doit pas seulement contrôler son équipe, mais doit aussi entretenir une certaine proximité avec ses membres. La constitution d’un sentiment d’appartenance au sein de l’équipe permet ainsi de créer une véritable unité collective. Ce sentiment résulte par exemple de l’attention que va accorder le manager aux centres d’intérêts ou à la vie familiale de ses collaborateurs.
							   
							   <h3>Le sens de la responsabilisation</h3>
							   Jeter le blâme sur ses collaborateurs ne fait pas partie des habitudes du leader. Au contraire, un bon leader se montre toujours prêt à accepter sa part de responsabilité. Il ne voit pas l’échec comme une finalité mais plutôt comme un apprentissage. Selon Anthony Erb, une étude montre que la première condition nécessaire à la réussite de son leadership est de savoir surmonter ses échecs ! <br>
							   Une chose est sûre, pour un bon leader, toutes ces compétences riment avec réussite …
							   </p>
