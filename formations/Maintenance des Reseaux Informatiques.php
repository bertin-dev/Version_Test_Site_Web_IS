								 Un réseau informatique est un ensemble d’équipements informatiques reliés entre eux soit par des câbles physiques soit par des ondes afin de pouvoir communiquer entre eux de manière sécurisée. Le réseau informatique permet notamment d’assurer la communication entre les ordinateurs, les postes de travail ou encore les terminaux informatiques.
								
								
								<strong>La maintenance d’un réseau informatique permet de s’assurer de son bon fonctionnement et de le faire évoluer</strong><br>

Le propre d’un réseau informatique est d’évoluer constamment au gré des évolutions de l’entreprise. En effet, il est amené à être modifié en fonction de l’arrivée ou du départ de collaborateurs, du remplacement ou de l’ajout de matériel informatique que ce soient des ordinateurs eux-mêmes ou des périphériques, ainsi que des mises à jour ou de l’ajout de logiciels.<br>

De ce fait, la maintenance du réseau informatique permet de suivre ces évolutions et d’ajuster le réseau et ses paramètres en fonction des derniers changements tant en termes d’utilisations applicatives ou matérielles que des besoins en serveurs. Ceci est particulièrement le cas pour les grandes entreprises du fait de leurs besoins qui évoluent très vites, mais il ne faut pas négliger les enjeux de la maintenance informatique des PME qui ont également des besoins importants et qui évoluent probablement encore plus vites.<br>
<strong>S’assurer de la sécurité des données grâce à la maintenance du réseau informatique</strong><br>

La maintenance du réseau informatique est essentielle à une bonne sécurisation des données. C’est d’ailleurs un des principaux intérêts de la maintenance informatique qui permet de suivre et d’éviter les tentatives d’intrusions en mettant notamment à jour les systèmes d’exploitation des éléments du réseau.<br>

De surcroît, la maintenance d’un réseau informatique s’intéresse de près à la sauvegarde des données, puisque un de ses objectifs principaux est de mettre en place et de s’assurer du bon fonctionnement de la sauvegarde des données, permettant ainsi de récupérer l’historique des données en cas de panne ou de dysfonctionnement grave.<br>

La maintenance du réseau informatique d’une entreprise permet donc de s’assurer du bon fonctionnement du réseau informatique dans sa durée, limiter les pannes, et, en cas de panne, d’un rétablissement rapide du réseau sans perte de données
